void main(List<String> args) {
  const age1 = 64.1;
  const age2 = 30.2;
  print(age1 + age2);
  const double divided = age1 / age2;
  print(divided);
  const intDivided = age1 ~/ age2; // rounded to tenth
  print(intDivided); // inDivived = double
  int age = 30;
  print(--age); //29
  print(++age); //30
  print(-age); //-30
  print(!true);
  const myTrueValue = true;
  print(!myTrueValue);
  print(~1); // -2
}
