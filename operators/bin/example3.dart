void main(List<String> args) {
  const age = 50;
  print(age + 20);
  print(age * 20);
  print(age / 20); // :
  print(age ~/ 20);
  print(age % 20); // remainder
  print(age == 20); // is equal
  print(age != 20); // is not equal
  print(age > 20);
  print(age < 20);
  print(age >= 20);
  print(age <= 20);
  print(age & 20);
  print(age << 20); // bitwise left shift
  print(age >> 20); // bitwse right shift
}
